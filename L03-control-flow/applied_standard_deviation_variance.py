from statistics import mean, stdev, variance

Investment1 = [5, 5, 5, 5, 5, 5, ]
Investment2 = [12, -2, 10, 0, 7, 3, ]

print(f'investment 1 mean: {mean(Investment1)}')
print(f'investment 2 mean: {mean(Investment2)}')

print(f'investment 1 standard deviation: {stdev(Investment1)}')
print(f'investment 2 standard deviation: {stdev(Investment2)}')
